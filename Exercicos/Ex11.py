#Faca um rograma que leia a largura e a altura de uma parede em mentros, calcule a sua area e sua quantidade de tinta necessaria para pinta-la, sabendo que cada litro de tinta, pinta uma area de 2m^2.
largura = float(input('Qual a largura da parede: '))
altura = float(input('Qual a altura da parede: '))
area = largura*altura
baldetinta = area/2
print('A área total da parede é: {}'.format(area))
print('Vai precisar de {} litros de tinta'.format(baldetinta))

                        




